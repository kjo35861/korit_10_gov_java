package study.ch06;

public class Main_Switch {
    public static void main(String[] args) {
        char c = 'a';

        switch (c) {
            case 'a':
                System.out.println("a를 선택하셨습니다.");
            case 'b':
                System.out.println("b를 선택하셨습니다.");
                break;
            case 'c':
                System.out.println("c를 선택하셨습니다.");
            case 'd':
                System.out.println("d를 선택하셨습니다.");
        }







    }
}
